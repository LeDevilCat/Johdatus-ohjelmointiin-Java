package TEH_10;

public class VariableIncrement {
    public static void main(String[] args) {
        int beginning = 0; // Alkuarvo
        
        System.out.println("Alkuarvo: " + beginning);
        beginning++; // Lisää 1 alkuarvoon
        System.out.println("Arvo 1: " + beginning);
        beginning++; // Lisää 1 alkuarvoon uudelleen
        System.out.println("Arvo 2: " + beginning);
        beginning++; // Lisää 1 alkuarvoon kolmannen kerran
        System.out.println("Arvo 3: " + beginning);
        beginning++; // Lisää 1 alkuarvoon neljännen kerran
        System.out.println("Arvo 4: " + beginning);
        beginning++; // Lisää 1 alkuarvoon viidennen kerran
        System.out.println("Arvo 5: " + beginning);
        beginning++; // Lisää 1 alkuarvoon kuudennen kerran
        System.out.println("Arvo 6: " + beginning);
        beginning++; // Lisää 1 alkuarvoon seitsemännen kerran
        System.out.println("Arvo 7: " + beginning);
        beginning++; // Lisää 1 alkuarvoon kahdeksannen kerran
        System.out.println("Arvo 8: " + beginning);
        beginning++; // Lisää 1 alkuarvoon yhdeksännen kerran
        System.out.println("Arvo 9: " + beginning);
        beginning++; // Lisää 1 alkuarvoon kymmenennen kerran
        System.out.println("Arvo 10: " + beginning);


    }
}
