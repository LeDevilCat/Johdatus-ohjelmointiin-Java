package TEH_03;

public class CommentPractice {
    public static void main(String[] args) {
        // Tämä on yksirivinen kommentti, joka selittää, mitä seuraava rivi tekee.
        System.out.println("Tämä ohjelma tulostaa tekstiä konsoliin.");
        System.out.println("Ohjelmassa on yhden rivin, usean rivin ja javaDoc-kommentteja.");

        /*
         Tämä on monirivinen kommentti.
         Se voi sisältää useita rivejä tekstiä.
         Kommentit eivät vaikuta ohjelman toimintaan.
         */
        System.out.println("Kommentit ovat hyödyllisiä ohjelman lukemisen helpottamiseksi.");
        System.out.println("Kommentit eivät näy ohjelman tulosteessa.");
        /**
         * Tämä on JavaDoc-kommentti.
         * Se on eroaa tavallisesta monirivisestä kommentista siten,
         * että se on tarkoitettu dokumentaation luomiseen.
         */
    }
}